"""Инструмент взаимодействия с пользователем (доступен только оркестратору)."""
from __future__ import annotations

from langchain_core.tools import tool


@tool
def ask_user(question: str) -> str:
    """Задать уточняющий вопрос пользователю и получить текстовый ответ.

    Используй при неоднозначности задачи, скоупа анализа или приоритетов.
    Не выдумывай скоуп сам — спроси.
    """
    print(f"\n[ВОПРОС ОТ ОРКЕСТРАТОРА] {question}")
    return input("Ваш ответ: ").strip()
